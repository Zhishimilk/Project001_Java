package com.bilibili.demo1;

public class DemoList1 {

    double money;
    int id;

    public DemoList1() {
    }

    public DemoList1(double money, int id) {
        this.money = money;
        this.id = id;
    }

    public double getMoney() {
        return money;
    }

    public void setMoney(double money) {
        this.money = money;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
